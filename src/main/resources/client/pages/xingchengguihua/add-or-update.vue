				riqi: '',
				didian: '',
				huodong: '',
				jiaotong: '',
				zhusu: '',
				yusuanfeiyong: '',
				zhuyaoshixiang: '',
				userid: '',
				},
				// 登录用户信息
				user: {},
                ro:{
                   riqi : false,
                   didian : false,
                   huodong : false,
                   jiaotong : false,
                   zhusu : false,
                   yusuanfeiyong : false,
                   zhuyaoshixiang : false,
                   userid : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect
		},
		watch:{
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
